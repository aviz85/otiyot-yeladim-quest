/* --- the assembly mini-puzzle: pick pages in order א, ב, ג --- */
async function assembleArticle(){
  // round 1 — need א
  while(true){
    await G.say('yossi','איזה עמוד ראשון?');
    const a=await G.choice(['עמוד ב','עמוד א','עמוד ג']);
    if(a===1){ await G.say('yossi','א! כמובן! ההתחלה של הכול. גם של המילה ״אבטיח״, אבל לא נתפזר.'); break; }
    if(a===0) await G.say('yossi','ב לפני א?! זה כמו לנעול נעליים לפני גרביים! עוד פעם, בנחת.');
    else await G.say('yossi','ג ראשון? הקוראים יתחילו מהסוף של האמצע! נסה שוב, אלוף.');
    G.sfx('no');
  }
  // round 2 — need ב
  while(true){
    await G.say('yossi','ועכשיו — מי השני?');
    const b=await G.choice(['עמוד ג','עמוד ב']);
    if(b===1){ await G.say('yossi','ב! בול! אתה שומע, בצלאל? הילד מעמד טבעי!'); break; }
    await G.say('yossi','ג באמצע זה לא ג. זה בלגן עם שאיפות. עוד ניסיון!');
    G.sfx('no');
  }
  // round 3 — only ג left, but ceremony matters
  await G.say('yossi','ונשאר...?');
  await G.choice(['עמוד ג!']);
  await G.say('yossi','ג!! א-ב-ג! סדר מושלם! תראה איך הם נשכבים על שולחן האור... כמו שלושה אחים שחזרו הביתה.');
  G.del('page1'); G.del('page2'); G.del('page3');
  G.add('article');
  G.sfx('ding');
  await G.say('yossi','הנה! כתבה שלמה, מיושרת, עם שוליים שהיו מרגשים את הסבא-רבא שלי מהדפוס.');
  await G.say('noam','היא... באמת יפה. אפילו החור במסך נראה גאה.');
  await G.say('yossi','ועכשיו — מה אתה עוד עושה פה?! לדפוס! לשמעון! רוץ!! ובלי לקפל!!');
}
